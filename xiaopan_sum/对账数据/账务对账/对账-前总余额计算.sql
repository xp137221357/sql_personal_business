
-- 其实两个时间点可以相互矫正
-- 0点
SELECT ITEM_TYPE ITEM_TYPE, SUM(ACCT_BALANCE + FREEZE_MONEY) / 100 BALANCE
FROM V_ACCOUNT_ITEM as of timestamp trunc(sysdate)
where ITEM_TYPE in (1001, 1015)
and ACC_NAME not in ('3472351858331386256','6149208545176280651','8270936710946839603')
group by ITEM_TYPE;
 
 -- 12点
SELECT ITEM_TYPE ITEM_TYPE, SUM(ACCT_BALANCE + FREEZE_MONEY) / 100 BALANCE
FROM V_ACCOUNT_ITEM as of timestamp trunc(sysdate)+1/2
where ITEM_TYPE in (1001, 1015)
and ACC_NAME not in ('3472351858331386256','6149208545176280651','8270936710946839603')
group by ITEM_TYPE;